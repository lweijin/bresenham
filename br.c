void data_init(void){
  int i,j;
  for (j=0;j<MAXY;j++) for (i=0;i<MAXX;i++) paint(i,j,'.');
}
void data_print(void){
  int i,j;
  for (j=0;j<MAXY;j++)
    for (i=0;i<MAXX;i++)
      printf("%c%c", data[i][j], i==MAXX-1?'\n':' ');
  printf("\n");
}

