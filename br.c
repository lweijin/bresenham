/*
Теперь научусь рисовать отрезки с dy<0 и dx<0
Шаги делаю в нужную сторону, а отклонение считаю,
как если бы все было положительным...
*/

void line4(int x1, int y1, int x2, int y2){
  int dx=x2-x1, dy=y2-y1;
  int e;
  int sx=dx>0;
  int sy=dy>0;

  if (!sx) dx=-dx;
  if (!sy) dy=-dy;

  int s=dx>dy;

  paint(x1,y1,'F');
  if (s){
    e = (dy<<1)-dx;
    while (x1!=x2){
      if (e<0){
        sx?x1++:x1--; e+=dy<<1;
        paint(x1,y1,'X');
      }
      else {
        sy?y1++:y1--; e-=dx<<1;
      }
    }
  }
  else {
    e = (dx<<1)-dy;
    while (y1!=y2){
      if (e<0){
        sy?y1++:y1--; e+=dx<<1;
        paint(x1,y1,'Y');
      }
      else {
        sx?x1++:x1--; e-=dy<<1;
      }
    }
  }
}

